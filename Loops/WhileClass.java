package loopClass;

public class WhileClass {

	public static void main(String[] args) {
		/*
		 * While loop is entry control loop 
		 * it will check first condition if it satisfy then only execute the block
		 */
		int x = 1;
		while(x<=10)
		{
			System.out.print(x+"  ");
			x++;
		}

	}

}
