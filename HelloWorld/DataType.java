package myPackage;

public class DataType {

	public static void main(String[] args) {
		// Primitive  Data Types
		byte num1 = 5; //8 bit
		short num2= 125; // 2 byte
		int  num3 = 578694; // 4byte
		long num4 = 45896123; // 8 byte
		float num5 = 29.21f; // 4 byte
		double num6 = 123.569; //8 byte
		char st = 'P'; 
		boolean flag = true; // 1 bit 
		
		
		System.out.println("The value of Byte is " +num1);
		System.out.println("The value of short is " +num2);
		System.out.println("The value Integer is " +num3);
		System.out.println("The value of Long is " +num4);
		System.out.println("The value of Float is " +num5);
		System.out.println("The value of Double is " +num6);
		System.out.println("The value of Character is " +st);
		System.out.println("The value of Boolean is " +flag);
		
	}

}
